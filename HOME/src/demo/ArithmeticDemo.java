package demo;

public class ArithmeticDemo {
	public static void main (String[]args) {
		
		int result = 1+2;
		result = result -1; // 3-1 = 2
		result = result*2; // 2*2= 4
		result=result/2; // 4 /2=2 
		result = result +8; // 2+8=10
		result=result%7;   // 10%7 = 3
		
		System.out.println(result);
		
	}

}
